-- Profitability Analysis: Calculate total net profit per account:
SELECT t.AccountID,
       c.firstname,
       c.lastname,
       sum(CASE
               WHEN t.TransactionType = 'credit' THEN Amount
               ELSE 0
           END)- sum(CASE
                         WHEN t.TransactionType = 'debit' THEN Amount
                         ELSE 0
                     END) AS NetProfit
FROM Transactions T
JOIN ACCOUNT A ON t.AccountID = a.AccountID
JOIN Customer C ON a.CustomerID = c.CustomerID
GROUP BY t.AccountID,
         c.firstname,
         c.lastname
ORDER BY t.AccountID

-- Monthly Transaction Summary
