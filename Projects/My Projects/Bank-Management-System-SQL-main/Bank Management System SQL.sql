/*Create a trigger to automatically update the Balance in the Account table whenever a transaction is 
inserted into the Transaction table*/

CREATE TRIGGER UpdateAccountBalance
ON Transactions
AFTER INSERT
AS
BEGIN
    UPDATE Account
    SET Balance = Balance + i.Amount
    FROM Account a
    INNER JOIN inserted i ON a.AccountID = i.AccountID
    WHERE i.TransactionType = 'Credit';

    UPDATE Account
    SET Balance = Balance - i.Amount
    FROM Account a
    INNER JOIN inserted i ON a.AccountID = i.AccountID
    WHERE i.TransactionType = 'Debit';
END;

